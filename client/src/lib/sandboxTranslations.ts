// Hook utilitaire pour les traductions sandbox
export const useSandboxTranslation = (language: 'fr' | 'en') => {
  const t = (key: string) => {
    const keys = key.split('.');
    let value: any = sandboxTranslations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  };
  
  return t;
};